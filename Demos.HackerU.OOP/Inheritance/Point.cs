using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Demos.HackerU.OOP.Inheritance
{
    public  class Point
    {
        int X { get; set; }
        int Y { get; set; }

        public Point(int x,int y)
        {
            X = x;
            Y = y;
        }

        public Point()
        {
            X = 0;
            Y = 0;
        }











    }
}
